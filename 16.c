char mult(char num[], int len_num, int n) {
    static int pattern[] = {0,1,0,-1};
    int sum = 0;
    for (int i = 0; i < len_num; i++) {
        sum += num[i] * pattern[((i+1)/n)%4];
    }
    int v = abs(sum);
    return (char) v - 10*(v/10);
}

